#include <iostream>
#include <iomanip>
#include <cmath>
#include <vector>
#include <string>

// 引入 Pinocchio 头文件
#include "pinocchio/multibody/model.hpp"
#include "pinocchio/multibody/data.hpp"
#include "pinocchio/algorithm/aba.hpp"

int main()
{
    // ==========================================
    // 1. 创建机器人模型
    // ==========================================
    pinocchio::Model model;

    // Pinocchio 默认重力是 (0, 0, -9.81)。
    // 这与你在 C++ 中设置基座 a_parent[5] = 9.81 是等效的物理体系，无需更改。
    // 如果你在手写算法中关闭了重力，请解除下面这行的注释：
    // model.gravity.setZero(); 

    // ==========================================
    // 2. 准备 6 轴物理参数 (MDH, 质量, 质心, 惯性张量)
    // ==========================================
    const int DOF = 6;
    
    double alpha[DOF] = {
        0.0, 
        -90.0 * M_PI / 180.0, 
        180.0 * M_PI / 180.0, 
        -90.0 * M_PI / 180.0, 
        90.0 * M_PI / 180.0, 
        -90.0 * M_PI / 180.0
    };
    double a[DOF] = {0.0, 0.0, 0.403112, -0.05, 0.0, 0.0};
    double d[DOF] = {0.328, 0.0, 0.4, 0.4, -0.136, 0.1035};

    double mass[DOF] = {
        3.16699999486378, 3.89505775503759, 1.55161965449885, 
        1.88199999547530, 1.34621855234916, 0.633999997351584
    };

    // 质心位置 (直接代入除以质量的公式，让编译器算)
    double com[DOF][3] = {
        { 3.48369999434000e-05/mass[0], -0.0253106639589312/mass[0], -0.291864385526951/mass[0] },
        { 0.659449401868420/mass[1],    -0.0651938216320210/mass[1], -0.271940703633802/mass[1] },
        {-0.00157764514450908/mass[2],   0.153969960151856/mass[2],  -0.00930606042947241/mass[2]},
        {-4.70521423066045e-05/mass[3], -0.0205570880251570/mass[3], -0.134124493621368/mass[3] },
        {-0.00190221636650860/mass[4],   0.0159457855937173/mass[4], -0.0281550840659444/mass[4] },
        { 4.62815646042645e-05/mass[5],  5.00861139672133e-05/mass[5], -0.0284368017495592/mass[5] }
    };

    // 质心处的 3x3 惯性张量 (拉平为一维数组以便循环赋值)
    double I[DOF][9] = {
        {0.058267910813839, -3.715826953316560e-07, -1.05949175179955e-06, -3.715826953316560e-07, 0.0576103483714700, -0.00456914016524626, -1.05949175179955e-06, -0.00456914016524626, 0.00607968319952077},
        {0.0380304151034530, 0.0419599871875192, 0.0436322529474843, 0.0419599871875192, 0.263486545009477, -0.00383736352324302, 0.0436322529474843, -0.00383736352324302, 0.250878539307793},
        {0.0359185029001311, 0.00357521327138579, 0.000314442104644629, 0.00357521327138579, 0.00328728806178666, 0.00270560721315391, 0.000314442104644629, 0.00270560721315391, 0.0354951529635439},
        {0.0231030453535813, -2.36395069266735e-06, -2.48326590911455e-06, -2.36395069266735e-06, 0.0220785414360184, -0.00290398198945597, -2.48326590911455e-06, -0.00290398198945597, 0.00301282626685543},
        {0.00568553742802938, 1.20535594054042e-05, -4.97420578495685e-05, 1.20535594054042e-05, 0.00444010179654207, 0.000717862262034322, -4.97420578495685e-05, 0.000717862262034322, 0.00277209865650963},
        {0.00227249981806468, -1.38365624835237e-06, 2.97586698701961e-06, -1.38365624835237e-06, 0.00227800923976532, 3.82651246364623e-06, 2.97586698701961e-06, 3.82651246364623e-06, 0.000447957334568561}
    };

    // ==========================================
    // 3. 构建动力学树 (应用你的 MDH 解析公式)
    // ==========================================
    pinocchio::JointIndex parent_id = 0; // 0 代表基座

    for (int i = 0; i < DOF; ++i) 
    {
        pinocchio::SE3 placement;
        
        // 【核心】完全保留你的 MDH 解析式计算法则
        placement.translation() << a[i], -d[i] * std::sin(alpha[i]), d[i] * std::cos(alpha[i]);
        placement.rotation() = Eigen::AngleAxisd(alpha[i], Eigen::Vector3d::UnitX()).toRotationMatrix();
        
        // 添加旋转关节
        parent_id = model.addJoint(parent_id, pinocchio::JointModelRZ(), placement, "Joint" + std::to_string(i+1));

        // 构造连杆惯性矩阵
        Eigen::Matrix3d I_mat;
        I_mat << I[i][0], I[i][1], I[i][2],
                 I[i][3], I[i][4], I[i][5],
                 I[i][6], I[i][7], I[i][8];
                 
        pinocchio::Inertia inertia(mass[i], Eigen::Vector3d(com[i][0], com[i][1], com[i][2]), I_mat);
        
        // 绑定连杆物理属性到关节
        model.appendBodyToJoint(parent_id, inertia, pinocchio::SE3::Identity());
    }

    // ==========================================
    // 4. 定义状态向量：位置 q, 速度 v, 力矩 tau
    // ==========================================
    Eigen::VectorXd q(model.nq);
    q << 0.0 * M_PI / 180.0, 
        -0.0 * M_PI / 180.0, 
         0.0 * M_PI / 180.0, 
         0.0 * M_PI / 180.0, 
        -0.0 * M_PI / 180.0, 
         0.0 * M_PI / 180.0;

    Eigen::VectorXd v(model.nv);
    v << 0, 0, 0, 0, 0, 0;

    Eigen::VectorXd tau(model.nv);
    tau.setZero(); // 等价于 tau << 0, 0, 0, 0, 0, 0;

    // ==========================================
    // 5. 创建 Data 结构并运行 ABA
    // ==========================================
    pinocchio::Data data(model);
    pinocchio::aba(model, data, q, v, tau);

    // ==========================================
    // 6. 打印结果，验证 ddq
    // ==========================================
    std::cout << std::fixed << std::setprecision(6);
    std::cout << "============= Pinocchio ABA 6-Axis Result =============" << std::endl;
    std::cout << "q (rad)   : " << q.transpose() << std::endl;
    std::cout << "dq (rad/s): " << v.transpose() << std::endl;
    std::cout << "tau (N.m) : " << tau.transpose() << std::endl;
    std::cout << "-------------------------------------------------------" << std::endl;
    
    for (int i = 0; i < DOF; ++i) {
        std::cout << "ddq [" << i << "]   = " << std::setw(12) << data.ddq[i] << " (rad/s^2)" << std::endl;
    }
    std::cout << "=======================================================" << std::endl;

    return 0;
}