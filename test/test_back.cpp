                in_1[0][3]*in_2[3][2] + in_1[0][4]*in_2[4][2] + in_1[0][5]*in_2[5][2];
    out[0][3] = in_1[0][0]*in_2[0][3] + in_1[0][1]*in_2[1][3] + in_1[0][2]*in_2[2][3] +
                in_1[0][3]*in_2[3][3] + in_1[0][4]*in_2[4][3] + in_1[0][5]*in_2[5][3];
